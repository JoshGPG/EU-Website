<!-- Course Description -->
<h2 class="nordic-section-title">Course Description</h2>

<div class="paddle-program-detail">
    <p>The 2025 Turkey Day Trail Trot course is one of our favorites on the east side of Battle Creek Regional Park. A few challenging hills on a 5K double loop course. Runners will run clockwise on the outer loop and counter clockwise on the inner loop.</p>
    <p>3K kids run starts with the 5K runners then they get to take the short cut to the finish.</p>
    <p>Be prepared for a mix of paved trail and off-pavement cross country ski trails including a dash of Mother Nature in the mix &mdash; could be warm and sunny then again, it has snowed early. The terrain is generally rolling and would not be described as flat.</p>
    <p>The Turkey Day Trail Trot is welcoming to calm, friendly dogs. Please use a fixed, non-extendable leash and be aware of other runners. Recommend to start at the back of the pack.</p>
</div>

